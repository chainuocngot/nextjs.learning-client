import type { Metadata } from "next"
import { Inter } from "next/font/google"
import "./globals.css"
import { ThemeProvider } from "@/components/theme-provider"
import Header from "@/components/header"
import { Toaster } from "@/components/ui/sonner"
import AppProvider from "@/app/app-provider"
import { cookies } from "next/headers"
import SlideSession from "@/components/slide-session"
import accountApiRequest from "@/api-requests/account"
import { AccountResType } from "@/schemaValidations/account.schema"

const inter = Inter({
  subsets: ["vietnamese"],
})

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  const cookieStore = await cookies()
  const sessionToken = cookieStore.get("sessionToken")?.value

  let me: AccountResType["data"] | null = null
  if (sessionToken) {
    const data = await accountApiRequest.me(sessionToken)
    me = data.payload.data
  }

  return (
    <html lang="en" suppressHydrationWarning>
      <body className={inter.className}>
        <Toaster />
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <AppProvider initialSessionToken={sessionToken} me={me}>
            <Header me={me} />
            {children}
            <SlideSession />
          </AppProvider>
        </ThemeProvider>
      </body>
    </html>
  )
}
